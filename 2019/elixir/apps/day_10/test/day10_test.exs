defmodule Day10Test do
  use ExUnit.Case
  doctest Day10, import: true

  test "find_best_position_and_count" do
    assert Day10.find_best_position_and_count("
      ......#.#.
      #..#.#....
      ..#######.
      .#.#.###..
      .#..#.....
      ..#....#.#
      #..#....#.
      .##.#..###
      ##...#..#.
      .#....####
    ") === {{5, 8}, 33}

    assert Day10.find_best_position_and_count("
      #.#...#.#.
      .###....#.
      .#....#...
      ##.#.#.#.#
      ....#.#.#.
      .##..###.#
      ..#...##..
      ..##....##
      ......#...
      .####.###.
    ") === {{1, 2}, 35}

    assert Day10.find_best_position_and_count("
      .#..#..###
      ####.###.#
      ....###.#.
      ..###.##.#
      ##.##.#.#.
      ....###..#
      ..#.#..#.#
      #..#.#.###
      .##...##.#
      .....#.#..
    ") === {{6, 3}, 41}

    assert Day10.find_best_position_and_count("
      .#..##.###...#######
      ##.############..##.
      .#.######.########.#
      .###.#######.####.#.
      #####.##.#.##.###.##
      ..#####..#.#########
      ####################
      #.####....###.#.#.##
      ##.#################
      #####.##.###..####..
      ..######..##.#######
      ####.##.####...##..#
      .#####..#.######.###
      ##...#.##########...
      #.##########.#######
      .####.#.###.###.#.##
      ....##.##.###..#####
      .#.#.###########.###
      #.#.#.#####.####.###
      ###.##.####.##.#..##
    ") === {{11, 13}, 210}
  end

  @tag :skip
  test "process part 1" do
    assert Day10.process_part1(Util.get_input()) === :answer
  end

  @tag :skip
  test "process part 2" do
    assert Day10.process_part2(Util.get_input()) === :answer
  end
end
